type Checked = DropdownMenuCheckboxItemProps['checked'];
export function WithCheckboxItems() {
  const [showStatusBar, setShowStatusBar] = useState<Checked>(true);
  const [showLineNumbers, setShowLineNumbers] = useState<Checked>(false);
  const [showMinimap, setShowMinimap] = useState<Checked>(false);

  return (
    <StoryContainer>
      <DropdownMenu>
        <DropdownMenuTrigger asChild>
          <Button variant="outline">Open</Button>
        </DropdownMenuTrigger>
        <DropdownMenuContent className="w-56">
          <DropdownMenuLabel>Appearance</DropdownMenuLabel>
          <DropdownMenuSeparator />
          <DropdownMenuCheckboxItem checked={showStatusBar} onCheckedChange={setShowStatusBar}>
            Status Bar
          </DropdownMenuCheckboxItem>
          <DropdownMenuCheckboxItem
            checked={showLineNumbers}
            onCheckedChange={setShowLineNumbers}
            disabled
          >
            Line Numbers
          </DropdownMenuCheckboxItem>
          <DropdownMenuCheckboxItem checked={showMinimap} onCheckedChange={setShowMinimap}>
            Minimap
          </DropdownMenuCheckboxItem>
        </DropdownMenuContent>
      </DropdownMenu>
    </StoryContainer>
  );
}

